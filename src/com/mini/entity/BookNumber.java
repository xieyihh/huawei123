package com.mini.entity;



/**
 * 书籍数量
 * 
 */
public class BookNumber {
	// 获取到的凭证
	private int id;
	//
	private String isbn;
	// 书籍的数量
	private int number;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
	
}