package com.mini.form;



public class ActivityStatusForm {

	private String id;/* 体检id */

	private String status;	/* 活动开始的内容标志内容  */
	private String activityid; /*活动的id*/
	private String activityname; /*活动的id*/
	private String statusName; /*活动的id*/
	
	public String getStatusName() {
		return statusName;
	}
	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getActivityid() {
		return activityid;
	}
	public void setActivityid(String activityid) {
		this.activityid = activityid;
	}
	public String getActivityname() {
		return activityname;
	}
	public void setActivityname(String activityname) {
		this.activityname = activityname;
	}
	
	
	
	
}
